function [x, y, chmap] = cf_getchannelmap(probetype, shankdistance)
%getchannelmap
%ALP 5/5/2022
%ALP 1/16/2023 moved to prospective coding directory

if strcmp(probetype, 'Takahashi')
    %L -> R is bottom -> Top of each column of channels
    chmapShank1 = [ ...
        24 28 29 0 1 2 ...
        26 5 30 7 3 23 4 ...
        21 20 6 17 19 22 ...
        12 14 8 16 10 18 15 ...
        31 25 27 13 11 9 ...
        ]; %end shank 1
    
    chmapShank2 = [ ...
        0 6 4 18 20 22 ...
        19 17 23 15 21 13 16 ...
        11 10 14 25 9 12 ...
        5 26 1 24 28 8 27 ...
        7 3 2 31 30 29 ...
        ]; %end shank 2
    
    chmap = [chmapShank1 chmapShank2+32]; %0:31 is Shank1, 32:63 is Shank2
    
    
    %%% x coords
    %   contacts are in squares of 20um sides so distance horizontally in each row
    %   is a^2+b^2 = c^2 -> sqrt(20^2+20^2) = sqrt(800)
    %   max distance is 3 contacts/row so max distance is sqrt(800)*2
    %   knowing evenly spaced distance between each is sqrt(800)*2/4 or
    %   sqrt(800)/2
    %
    if isempty(shankdistance)
        plotdistance = 200; %default
    else
        plotdistance = shankdistance; %smaller than the actual which is 200 for easier visualization of multiple probes
    end
    
    x = [ ...
        ones(6,1); ...
        sqrt(800)/2+ones(7,1); sqrt(800)/2*2+ones(6,1); ...
        sqrt(800)/2*3+ones(7,1); sqrt(800)/2*4+ones(6,1); ... %end shank 1
        plotdistance+ones(6,1); %point to point is 200um, so add plotdistance
        plotdistance+sqrt(800)/2+ones(7,1); plotdistance+sqrt(800)/2*2+ones(6,1); ...
        plotdistance+sqrt(800)/2*3+ones(7,1); plotdistance+sqrt(800)/2*4+ones(6,1) ... %end shank 2
        ];
    
    %%% y coords
    %   contacts are in 13 rows with length of 170um
    %   so each row is 170/12 um apart from each other
    
    y = [ ...
        1+(170/12):170/12*2:171-(170/12) 1:170/12*2:170+1 ...
        1+(170/12):170/12*2:171-(170/12) 1:170/12*2:170+1 ...
        1+(170/12):170/12*2:171-(170/12) ... %end shank 1
        1+(170/12):170/12*2:171-(170/12) 1:170/12*2:170+1 ...
        1+(170/12):170/12*2:171-(170/12) 1:170/12*2:170+1 ...
        1+(170/12):170/12*2:171-(170/12) ... % end shank 2
        ];
end
end

